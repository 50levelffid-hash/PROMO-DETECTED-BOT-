function getString(lang, key, vars = {}) {
  const langStrings = STRINGS[lang] || STRINGS.en;
  let str = langStrings[key] || STRINGS.en[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replaceAll(`{${k}}`, v);
  }
  return str;
}

